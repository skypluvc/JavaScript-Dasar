// variabel dengan let
let nama = "Gendies Kirana Larasati";

// tampilkan data
console.log(nama);

// ubah nilai variabel nama
nama = "Wiwok De Tok";
console.log(nama);

// variabel dengan var
var namaDepan = "Gendies";
console.log(namaDepan);
namaDepan = "Wiwok";
console.log(namaDepan);

// kelakukan dari let
let namaBelakang = "Larasati";
{
    let namaBelakang = "Tok";
    console.log(namaBelakang);
}
console.log(namaBelakang);

// kelakukan dari var
var namaTengah = "Kirana";
{
    var namaTengah = "De";
    console.log(namaTengah);
}
console.log(namaTengah);
 
